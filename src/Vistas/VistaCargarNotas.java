    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int filaseleccionada = dgvCursadas.getSelectedRow();
        if(filaseleccionada!=-1){
            Alumno alu =(Alumno)cboAlumnos.getSelectedItem();
            
            int idMateria = (Integer)modelo.getValueAt(filaseleccionada, 0);
            int nota = Integer.parseInt((String)modelo.getValueAt(filaseleccionada, 2));
            CursadaData cursadadata = new CursadaData(conexion);
            
            cursadadata.actualizarNotaCursada(alu.getId(), idMateria, nota);
            borrarFilasTabla();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

