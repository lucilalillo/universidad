    public void armaCabeceraTabla()
    {
        ArrayList<Object> columnas = new ArrayList<Object>();
        columnas.add("ID");
        columnas.add("Materia");
        columnas.add("Nota");
        for(Object it:columnas){
            modelo.addColumn(it);
        }
        dgvCursadas.setModel(modelo);
    }    
    
    public void borrarFilasTabla()
    {
       int a= modelo.getRowCount()-1;
        for(int i=a;i>=0;i--){
            modelo.removeRow(i);
        } 
    }
    
    public void cargarDatos()
    {
        Alumno selec = (Alumno)cboAlumnos.getSelectedItem();
        listacur = (ArrayList)cd.obtenerCursadasXAlumno(selec.getId());
        for(Cursada m: listacur)
          modelo.addRow(new Object[]{m.getMateria().getCod(),m.getMateria().getNombre(),m.getNota()});
    }

